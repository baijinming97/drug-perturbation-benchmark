#!/usr/bin/env python
"""Unified-pipeline shim for XPert.

Translates the unified CLI used by experiments/<task>/train_all.py
(--fold/--split_col/--output_dir/--max_epoch/--ablation_mode/--dev) into XPert's
native YAML-driven workflow, then post-processes the output into the unified
format consumed by experiments/_shared/evaluation/evaluate_one_fold.py.

XPert source code is NOT modified. This shim:
  1. Picks the right YAML config based on --ablation_mode:
       none    → models/XPert/configs/config_l1000.yaml
       zero    → models/XPert/configs/config_l1000_ablation_zero_all.yaml
       shuffle → models/XPert/configs/config_l1000_ablation_shuffle_all.yaml
     Pre-computed zero/shuffle drug arrays come from data/_xpert_ablation/
     (generated by step_generate_xpert_ablation_data in prepare_steps.py).
  2. Loads the YAML, overrides num_epochs (--max_epoch) and rewrites all data
     paths to absolute paths, writes a temp YAML at models/XPert/configs/.
  3. Runs `python train_xpert.py --config <temp> --nfold <split_col> ...`
     from cwd=models/XPert/ with --output_profile True.
  4. Reads {expt_dir}/predict_profile.npy (XPert's dict of y_true/y_pred/ctl_true)
     and writes unified format under {output_dir}/predictions/ + checkpoints/.
  5. Cleans up the temp YAML.
"""
from __future__ import annotations

# Suppress RDKit's verbose C++ logger BEFORE any rdkit-using import
# (otherwise GetExplicitValence deprecation spams stderr ~5M lines/run).
from rdkit import RDLogger
RDLogger.logger().setLevel(RDLogger.ERROR)
RDLogger.DisableLog('rdApp.*')

import argparse
import os
import shutil
import subprocess
import sys
import tempfile
import time
from pathlib import Path

import numpy as np
import yaml

REPO_ROOT = Path(__file__).resolve().parents[3]
XPERT_DIR = REPO_ROOT / "models" / "XPert"
ABL_DIR = REPO_ROOT / "data" / "_xpert_ablation"

ABL_TO_YAML = {
    "none":    "config_l1000",
    "zero":    "config_l1000_ablation_zero_all",
    "shuffle": "config_l1000_ablation_shuffle_all",
}


def parse_args():
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--fold", type=int, required=True, help="Fold index 0-4.")
    p.add_argument("--split_col", type=str, required=True,
                   help="adata.obs column carrying train/valid/test labels (e.g. nm_drug_blind_1).")
    p.add_argument("--ablation_mode", type=str, default="none",
                   choices=list(ABL_TO_YAML),
                   help="Data-level ablation. Maps to XPert's pre-computed ablation YAML.")
    p.add_argument("--output_dir", type=str, required=True,
                   help="Unified output directory (predictions/, metrics/, checkpoints/, logs/ go here).")
    p.add_argument("--max_epoch", type=int, default=None,
                   help="Override config['train']['num_epochs'] when set (e.g. 1 for verification).")
    p.add_argument("--dev", type=str, default="cuda:0", help="CUDA device.")
    p.add_argument("--seed", type=int, default=2024)
    p.add_argument("--dataset", type=str, default="l1000_sdst",
                   help="XPert --dataset arg (l1000_sdst, l1000_mdmt, ...).")
    p.add_argument("--drug_feat", type=str, default="unimol")
    p.add_argument("--use_gradscaler", type=str, default="True",
                   help="XPert flag (string True/False per its bool argparse).")
    return p.parse_args()


def absolutize_paths(cfg: dict) -> dict:
    """Rewrite all relative npy/h5ad paths in the YAML to absolute REPO_ROOT-rooted paths.

    XPert reads relative paths from cwd. Since we run from models/XPert/, naive
    relative paths would point to nonexistent files. Absolutizing avoids any
    cwd surprises and lets the shim be cwd-agnostic.
    """
    def fix(path):
        if path is None or os.path.isabs(path):
            return path
        return str(REPO_ROOT / path)

    # ATTN
    if "model" in cfg and "ATTN" in cfg["model"]:
        for k in ("ppi_gene_vector_path",):
            if k in cfg["model"]["ATTN"]:
                cfg["model"]["ATTN"][k] = fix(cfg["model"]["ATTN"][k])
    # HG
    if "model" in cfg and "HG" in cfg["model"]:
        for k in ("hg_path", "drug_hg_pretrained_embed_path",
                  "specific_pretrained_embed_path"):
            if k in cfg["model"]["HG"]:
                cfg["model"]["HG"][k] = fix(cfg["model"]["HG"][k])
    # dataset
    if "dataset" in cfg:
        for k in ("drug_unimol_path", "drug_KPGT_path", "drug_smi_path",
                  "drug_morgan_path", "l1000_sdst_data_root",
                  "l1000_mdmt_full_data_root", "PANACEA_data_root",
                  "CDSDB_data_root"):
            if k in cfg["dataset"]:
                cfg["dataset"][k] = fix(cfg["dataset"][k])
    return cfg


def remap_ablation_paths(cfg: dict, mode: str) -> dict:
    """Point the ablation arrays to data/_xpert_ablation/ (absolute) so we don't
    rely on the YAML's hardcoded experiment/ablation_infer/data/ relative path."""
    if mode == "zero":
        cfg["dataset"]["drug_unimol_path"] = str(ABL_DIR / "drugs_unimol_zero_all.npy")
        cfg["model"]["HG"]["drug_hg_pretrained_embed_path"] = str(ABL_DIR / "HG_drug_embeddings_zero.npy")
    elif mode == "shuffle":
        cfg["dataset"]["drug_unimol_path"] = str(ABL_DIR / "drugs_unimol_shuffle.npy")
        cfg["model"]["HG"]["drug_hg_pretrained_embed_path"] = str(ABL_DIR / "HG_drug_embeddings_shuffle.npy")
    return cfg


def write_temp_config(cfg: dict, tag: str) -> tuple[Path, str]:
    """Write the patched YAML to models/XPert/configs/_tmp_<tag>.yaml.
    Returns (temp_path, basename_without_ext) — XPert's --config arg expects the
    basename and looks under configs/ relative to its cwd (models/XPert/)."""
    XPERT_DIR.joinpath("configs").mkdir(exist_ok=True)
    name = f"_tmp_unified_{tag}_{os.getpid()}_{int(time.time())}"
    path = XPERT_DIR / "configs" / f"{name}.yaml"
    with open(path, "w") as f:
        yaml.safe_dump(cfg, f, sort_keys=False)
    return path, name


def postprocess_to_bench_format(xpert_expt_dir: Path, output_dir: Path,
                              h5ad_path: Path, split_col: str) -> None:
    """Convert XPert's dict-style predict_profile.npy → unified arrays.

    XPert (validate with --output_profile True) saves:
      {expt}/{fold_token}_predict_profile.npy = dict(y_true, y_pred, ctl_true, deg_true, deg_pred)
      where y_true / y_pred / ctl_true are RAW perturbed/control expression.
    Unified format (matches the 6 train_<M>.py shims):
      predictions/test_predictions.npy   (N, 978)  ← deg_pred  = y_pred - ctl_true
      predictions/test_ground_truth.npy  (N, 978)  ← deg_true  = y_true - ctl_true
      predictions/test_ctrl.npy          (N, 978)  ← ctl_true  (raw control)
      predictions/train_x_pert_mean.npy  (978,)    ← train perturbed mean
      predictions/test_sample_ids.csv    (N rows)  ← drug_id (=pert_idx), cell_id (=cell_idx)
      predictions/per_drug_pcc.csv       (D rows)  ← mean PCC per drug
    The DEG convention is what evaluate_predictions(y_deg_true, y_deg_pred, ...)
    in evaluate_all.py expects as its first two args.
    """
    sys.path.insert(0, str(Path(__file__).resolve().parents[1]))
    from postproc_helpers import write_sidecar_files

    pred_dir = output_dir / "predictions"
    pred_dir.mkdir(parents=True, exist_ok=True)

    # Locate the predict_profile dict — XPert names it {fold}_predict_profile.npy.
    candidates = list(xpert_expt_dir.glob(f"*_predict_profile.npy"))
    if not candidates:
        raise FileNotFoundError(
            f"XPert did not write any *_predict_profile.npy under {xpert_expt_dir}. "
            "Was --output_profile True? Did training finish?"
        )
    profile_path = candidates[0]
    print(f"  [post] reading {profile_path.name}")
    profile = np.load(profile_path, allow_pickle=True).item()

    y_true = np.asarray(profile["y_true"], dtype=np.float32)
    y_pred = np.asarray(profile["y_pred"], dtype=np.float32)
    ctl_true = np.asarray(profile["ctl_true"], dtype=np.float32)
    deg_true = np.asarray(profile.get("deg_true", y_true - ctl_true), dtype=np.float32)
    deg_pred = np.asarray(profile.get("deg_pred", y_pred - ctl_true), dtype=np.float32)

    np.save(pred_dir / "test_predictions.npy", deg_pred)
    np.save(pred_dir / "test_ground_truth.npy", deg_true)
    np.save(pred_dir / "test_ctrl.npy", ctl_true)

    write_sidecar_files(pred_dir, deg_true, deg_pred, h5ad_path, split_col)
    print(f"  [post] wrote 6-file unified-format predictions to {pred_dir}")

    # checkpoints — XPert saves an early-stopped checkpoint into expt with a
    # name like `<nfold>_fold_early_stop.pth`; older builds called it
    # `best_model*.pt*`. Match either, prefer best_model when both exist.
    ckpts = (list(xpert_expt_dir.glob("**/best_model*.pt*"))
             + list(xpert_expt_dir.glob("**/*early_stop.pth")))
    if ckpts:
        ck_dir = output_dir / "checkpoints"
        ck_dir.mkdir(exist_ok=True)
        shutil.copy(ckpts[0], ck_dir / "best_model.pt")


def cleanup_orphan_temp_yamls(grace_seconds: int = 3600) -> None:
    """Remove `_tmp_unified_*.yaml` files older than `grace_seconds` from
    models/XPert/configs/. The normal lifecycle removes its own temp YAML in a
    finally block, but a SIGKILL (e.g. SLURM TIMEOUT) skips finally and leaves
    the file behind. This sweep runs at startup so source-tree stays clean. The
    grace window protects concurrent shim invocations whose YAMLs are still in use.
    """
    cfg_dir = XPERT_DIR / "configs"
    if not cfg_dir.is_dir():
        return
    cutoff = time.time() - grace_seconds
    for stale in cfg_dir.glob("_tmp_unified_*.yaml"):
        try:
            if stale.stat().st_mtime < cutoff:
                stale.unlink()
                print(f"  [cleanup] removed orphan temp YAML: {stale.name}", flush=True)
        except OSError:
            pass


def main() -> int:
    cleanup_orphan_temp_yamls()
    args = parse_args()
    output_dir = Path(args.output_dir).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)
    xpert_expt_dir = output_dir / "_xpert_raw"
    xpert_expt_dir.mkdir(exist_ok=True)

    # --- Step 1: pick + load + patch YAML ----------------------------------
    base_yaml = XPERT_DIR / "configs" / f"{ABL_TO_YAML[args.ablation_mode]}.yaml"
    if not base_yaml.exists():
        print(f"FATAL: base YAML not found: {base_yaml}", file=sys.stderr)
        return 2
    with open(base_yaml) as f:
        cfg = yaml.safe_load(f)

    if args.max_epoch is not None:
        cfg.setdefault("train", {})["num_epochs"] = int(args.max_epoch)
        # Some configs gate batch-loss switching at init_epoch — keep <= num_epochs.
        if cfg["train"].get("init_epoch", 0) >= int(args.max_epoch):
            cfg["train"]["init_epoch"] = 0

    cfg = absolutize_paths(cfg)
    cfg = remap_ablation_paths(cfg, args.ablation_mode)

    tag = f"{args.ablation_mode}_e{args.max_epoch or 'def'}_fold{args.fold}"
    tmp_path, tmp_name = write_temp_config(cfg, tag)

    # --- Step 2: subprocess XPert ------------------------------------------
    cmd = [
        sys.executable, "train_xpert.py",
        "--mode", "train",
        "--model", "XPert",
        "--config", tmp_name,
        "--drug_feat", args.drug_feat,
        "--nfold", args.split_col,
        "--dataset", args.dataset,
        "--device", args.dev,
        "--seed", str(args.seed),
        "--use_gradscaler", args.use_gradscaler,
        "--output_profile", "True",
        "--expt_dir", str(xpert_expt_dir),
    ]
    print(f"  CMD  cd {XPERT_DIR} && {' '.join(cmd)}", flush=True)

    try:
        subprocess.run(cmd, check=True, cwd=str(XPERT_DIR))
    finally:
        # Best-effort cleanup of the temp YAML even on failure.
        try:
            tmp_path.unlink()
        except FileNotFoundError:
            pass

    # --- Step 3: post-process to unified format ----------------------------
    # Locate the h5ad: cfg already absolutized in absolutize_paths(). Map the
    # dataset CLI value to its data_root key (matches XPert's MyDataset).
    ds_to_key = {
        "l1000_sdst": "l1000_sdst_data_root",
        "l1000_mdmt": "l1000_mdmt_full_data_root",
        "panacea":    "PANACEA_data_root",
        "cdsdb_mdmt": "CDSDB_data_root",
    }
    h5ad_key = ds_to_key.get(args.dataset)
    if h5ad_key is None or h5ad_key not in cfg.get("dataset", {}):
        raise KeyError(f"No h5ad path for --dataset={args.dataset} in YAML")
    h5ad_path = Path(cfg["dataset"][h5ad_key])

    postprocess_to_bench_format(xpert_expt_dir, output_dir,
                             h5ad_path=h5ad_path, split_col=args.split_col)
    print(f"  [DONE] xpert/{args.ablation_mode}/fold_{args.fold} → {output_dir}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
