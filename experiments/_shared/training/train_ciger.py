# ── Batch iterator ─────────────────────────────────────────────────────────
# Mirrors DataReader.get_batch_data() (datareader.py:28-67) exactly:
#   shuffle indices -> slice -> convert_smile_to_feature -> create_mask_feature -> yield

def get_batch_data(feature, label, batch_size, shuffle, device,
                   ablation_mode="none"):
    """Yield (feature_dict, label_dict) batches.

    Calls original convert_smile_to_feature() and create_mask_feature() unchanged.
    """
    n = len(feature['drug'])
    if shuffle:
        index = torch.randperm(n).long().numpy()
    else:
        index = np.arange(n)

    for start_idx in range(0, n, batch_size):
        excerpt = index[start_idx:start_idx + batch_size]

        out_ft = dict()
        out_lb = dict()

        # Drug: SMILES → molecular graph (original function)
        drug_feat = convert_smile_to_feature(feature['drug'][excerpt], device)
        mask_feat = create_mask_feature(drug_feat, device)

        out_ft['drug'] = drug_feat
        out_ft['mask'] = mask_feat

        # Cell ID one-hot
        if 'cell_id' in feature:
            out_ft['cell_id'] = feature['cell_id'][excerpt]

        # Gene indices: [batch, 978] = arange(978) repeated
        bs = len(excerpt)
        out_ft['gene'] = torch.arange(978).repeat(bs).reshape(bs, 978).to(device)

        # Labels
        out_lb['real'] = label['real'][excerpt]
        out_lb['binary'] = label['binary'][excerpt]
        out_lb['binary_reverse'] = label['binary_reverse'][excerpt]

        yield out_ft, out_lb


# ── Data loading (replaces DataReader) ─────────────────────────────────────

def load_bench_data(h5ad_path, smi_path, gene_file, fold, device, ablation_mode, split_col_override=None):
    """Load the unified dataset and construct feature/label dicts matching DataReader format."""

    print("[1/4] Loading h5ad ...")
    adata = sc.read_h5ad(h5ad_path)
    x_pert = adata.X
    if hasattr(x_pert, 'toarray'):
        x_pert = x_pert.toarray()
    x_ctrl = adata.obsm['X_ctl']
    if hasattr(x_ctrl, 'toarray'):
        x_ctrl = x_ctrl.toarray()
    x_deg = (x_pert - x_ctrl).astype(np.float32)

    pert_idx = adata.obs['pert_idx'].values.astype(int)
    cell_names = adata.obs['cell_mfc_name'].values.astype(str)

    # Load SMILES mapping
    print("[2/4] Loading SMILES + gene features ...")
    idx2smi = np.load(smi_path, allow_pickle=True).item()

    # Gene features (frozen embedding) — original read_gene function
    gene_embed = read_gene(gene_file, 1107, device)

    # Split by cold-drug fold
    print("[3/4] Splitting data ...")
    split_col = split_col_override or f"nm_drug_blind_{fold + 1}"
    splits = adata.obs[split_col].values.astype(str)

    # Build SMILES array for all samples
    smiles_all = np.array([idx2smi[d] for d in pert_idx])

    # Shuffle ablation: permute drug identity
    if ablation_mode == 'shuffle':
        print("  [ABLATION] Shuffling drug identity (seed=131419)")
        unique_drugs = np.unique(pert_idx)
        rng = np.random.default_rng(seed=131419)
        perm = rng.permutation(len(unique_drugs))
        drug_map = dict(zip(unique_drugs, unique_drugs[perm]))
        shuffled_idx = np.array([drug_map[d] for d in pert_idx])
        smiles_all = np.array([idx2smi[d] for d in shuffled_idx])

    # Cell ID one-hot (auto-detect: >1 unique → use_cell_id=True)
    cell_id_set = sorted(list(set(cell_names)))
    cell_id_dict = {c: i for i, c in enumerate(cell_id_set)}
    num_cells = len(cell_id_set)
    use_cell_id = num_cells > 1
    print(f"  Cell types: {num_cells}, use_cell_id={use_cell_id}")

    # Percentile thresholds from ALL data (matches original data_utils.py:157-166)
    pos_threshold = np.quantile(x_deg, 0.95)
    neg_threshold = np.quantile(x_deg, 0.05)
    print(f"  Percentile thresholds: pos={pos_threshold:.4f}, neg={neg_threshold:.4f}")

    print("[4/4] Building feature/label dicts ...")
    data = {}
    for split_name, dataset_name in [('train', 'train'), ('valid', 'dev'), ('test', 'test')]:
        mask = splits == split_name
        n = mask.sum()

        # Feature dict
        ft = dict()
        ft['drug'] = smiles_all[mask]  # np.array of SMILES strings

        if use_cell_id:
            cell_onehot = np.zeros((n, num_cells), dtype=np.float32)
            for i, c in enumerate(cell_names[mask]):
                cell_onehot[i, cell_id_dict[c]] = 1.0
            ft['cell_id'] = torch.from_numpy(cell_onehot).to(device)

        # Label dict
        lb = dict()
        deg = x_deg[mask]
        lb['real'] = torch.from_numpy(deg).to(device)
        lb['binary'] = torch.from_numpy(
            (deg > pos_threshold).astype(np.float32)).to(device)
        lb['binary_reverse'] = torch.from_numpy(
            (deg < neg_threshold).astype(np.float32)).to(device)

        data[dataset_name] = (ft, lb)
        print(f"  {dataset_name}: {n} samples")

    # Also store metadata for postprocessing
    test_mask = splits == 'test'
    meta = {
        'pert_idx': pert_idx[test_mask],
        'cell_idx': adata.obs['cell_idx'].values.astype(int)[test_mask],
        'x_pert': x_pert[test_mask],
        'x_ctrl': x_ctrl[test_mask],
        'x_pert_train_mean': x_pert[splits == 'train'].mean(axis=0),
    }

    # Model config
    drug_dim = {'atom': 62, 'bond': 6}  # neural fp
    cell_id_dim = num_cells if use_cell_id else 0

    return data, gene_embed, drug_dim, use_cell_id, cell_id_dim, meta


def main():
    args = parse_args()
    fold = args.fold

    if args.output_dir:
        out_dir = args.output_dir
    elif args.ablation_mode == 'none':
        out_dir = os.path.join(RESULTS_BASE,
            f"main_benchmark/ciger/fold_{fold}")
    else:
        out_dir = os.path.join(RESULTS_BASE,
            f"main_ablation/ciger/{args.ablation_mode}/fold_{fold}")
    os.makedirs(out_dir, exist_ok=True)

    device = torch.device(args.dev if torch.cuda.is_available() else 'cpu')

    # ── Hyperparameters (identical to train.sh) ────────────────────────────
    fp_type = 'neural'
    label_type = 'real'
    loss_type = 'list_wise_rankcosine'
    batch_size = 64
    max_epoch = args.max_epoch
    lr = 0.003
    num_gene = 978

    print(f"{'='*60}")
    print(f"CIGER | Fold {fold} | Ablation: {args.ablation_mode}")
    print(f"Output: {out_dir}")
    print(f"Seed: {seed}, lr: {lr}, batch: {batch_size}, epochs: {max_epoch}")
    print(f"Loss: {loss_type}, Label: {label_type}, FP: {fp_type}")
    print(f"{'='*60}")

    # ── Load data ──────────────────────────────────────────────────────────
    data, gene_embed, drug_dim, use_cell_id, cell_id_dim, meta = \
        load_bench_data(args.h5ad_path, args.smi_path, args.gene_file,
                     fold, device, args.ablation_mode,
                     split_col_override=args.split_col)

    train_ft, train_lb = data['train']
    dev_ft, dev_lb = data['dev']
    test_ft, test_lb = data['test']

    print(f"#Train: {len(train_ft['drug'])}")
    print(f"#Dev: {len(dev_ft['drug'])}")
    print(f"#Test: {len(test_ft['drug'])}")

    # ── Model init (identical to train.py:138-142) ─────────────────────────
    initializer = torch.nn.init.xavier_uniform_
    model = CIGER(
        drug_input_dim=drug_dim,
        gene_embed=gene_embed,
        gene_input_dim=gene_embed.size()[1],
        encode_dim=512,
        fp_type=fp_type,
        loss_type=loss_type,
        label_type=label_type,
        device=device,
        initializer=initializer,
        pert_type_input_dim=None,
        cell_id_input_dim=cell_id_dim if use_cell_id else None,
        pert_idose_input_dim=None,
        use_pert_type=False,
        use_cell_id=use_cell_id,
        use_pert_idose=False,
    )
    model.to(device)

    # Zero ablation: register forward hook to zero NeuralFingerprint output
    if args.ablation_mode == 'zero':
        print("[ABLATION] Registering forward hook to zero drug encoder output")
        def zero_drug_output(module, input, output):
            if not hasattr(zero_drug_output, '_logged'):
                print(f"[HOOK] drug_fp output zeroed: shape={output.shape}, "
                      f"original_max={output.abs().max():.4f}")
                zero_drug_output._logged = True
            return torch.zeros_like(output)
        model.drug_fp.register_forward_hook(zero_drug_output)

    optimizer = torch.optim.Adam(model.parameters(), lr=lr)

    param_count = sum(p.numel() for p in model.parameters())
    print(f"Parameters: {param_count:,}")

    # ── Training (mirrors train.py:157-337 exactly) ────────────────────────
    best_dev_ndcg = float("-inf")
    best_epoch = 0
    score_list_dev = {'ndcg': []}
    score_list_test = {'ndcg': []}
    num_batch_train = math.ceil(len(train_ft['drug']) / batch_size)
    num_batch_dev = math.ceil(len(dev_ft['drug']) / batch_size)
    num_batch_test = math.ceil(len(test_ft['drug']) / batch_size)

    ckpt_path = os.path.join(out_dir, "checkpoints")
    os.makedirs(ckpt_path, exist_ok=True)
    ckpt_file = os.path.join(ckpt_path, "best_model.ckpt")

    train_start = time.time()

    for epoch in range(max_epoch):
        print("Iteration %d:" % (epoch + 1))

        # ── Train ──
        model.train()
        epoch_loss = 0
        for i, batch in enumerate(tqdm(
                get_batch_data(train_ft, train_lb, batch_size, shuffle=True,
                               device=device, ablation_mode=args.ablation_mode),
                total=num_batch_train)):
            ft, lb = batch
            drug = ft['drug']
            cell_id = ft.get('cell_id', None)
            gene = ft['gene']
            label = lb['real']

            optimizer.zero_grad()
            predict = model(drug, gene, None, cell_id, None)
            loss = model.loss(label, predict)
            loss.backward()
            optimizer.step()
            epoch_loss += loss.item()
        print('Train loss: %.4f' % (epoch_loss / (i + 1)))

        # ── Dev eval ──
        model.eval()
        label_binary_np = np.empty([0, num_gene])
        label_real_np = np.empty([0, num_gene])
        predict_np = np.empty([0, num_gene])
        with torch.no_grad():
            for i, batch in enumerate(tqdm(
                    get_batch_data(dev_ft, dev_lb, batch_size, shuffle=False,
                                   device=device, ablation_mode=args.ablation_mode),
                    total=num_batch_dev)):
                ft, lb = batch
                drug = ft['drug']
                cell_id = ft.get('cell_id', None)
                gene = ft['gene']
                predict = model(drug, gene, None, cell_id, None)

                label_binary = lb['binary']
                label_real = lb['real']
                label_binary_np = np.concatenate(
                    (label_binary_np, label_binary.cpu().numpy()), axis=0)
                label_real_np = np.concatenate(
                    (label_real_np, label_real.cpu().numpy()), axis=0)
                predict_np = np.concatenate(
                    (predict_np, predict.cpu().numpy()), axis=0)

            label_real_clamped = np.where(label_real_np < 0, 0, label_real_np)
            predict_clamped = np.where(predict_np < 0, 0, predict_np)
            ndcg_score = ndcg(label_real_clamped, predict_clamped)
            print('Dev NDCG: %.4f' % ndcg_score)
            score_list_dev['ndcg'].append(ndcg_score)

            if best_dev_ndcg < ndcg_score:
                best_dev_ndcg = ndcg_score
                best_epoch = epoch
                torch.save({'model_state_dict': model.state_dict(),
                            'optimizer_state_dict': optimizer.state_dict()},
                           ckpt_file)

        # ── Test eval ──
        label_binary_np = np.empty([0, num_gene])
        label_real_np = np.empty([0, num_gene])
        predict_np = np.empty([0, num_gene])
        with torch.no_grad():
            for i, batch in enumerate(tqdm(
                    get_batch_data(test_ft, test_lb, batch_size, shuffle=False,
                                   device=device, ablation_mode=args.ablation_mode),
                    total=num_batch_test)):
                ft, lb = batch
                drug = ft['drug']
                cell_id = ft.get('cell_id', None)
                gene = ft['gene']
                predict = model(drug, gene, None, cell_id, None)

                label_binary = lb['binary']
                label_real = lb['real']
                label_binary_np = np.concatenate(
                    (label_binary_np, label_binary.cpu().numpy()), axis=0)
                label_real_np = np.concatenate(
                    (label_real_np, label_real.cpu().numpy()), axis=0)
                predict_np = np.concatenate(
                    (predict_np, predict.cpu().numpy()), axis=0)

            label_real_clamped = np.where(label_real_np < 0, 0, label_real_np)
            predict_clamped = np.where(predict_np < 0, 0, predict_np)
            ndcg_score = ndcg(label_real_clamped, predict_clamped)
            print('Test NDCG: %.4f' % ndcg_score)
            score_list_test['ndcg'].append(ndcg_score)

    wall_time = time.time() - train_start
    print(f"\nBest dev NDCG: {best_dev_ndcg:.4f} at epoch {best_epoch + 1}")
    print(f"Test NDCG at best dev: {score_list_test['ndcg'][best_epoch]:.4f}")
    print(f"Wall time: {wall_time:.0f}s ({wall_time/60:.1f}min)")

    # ── Final test predictions with best checkpoint ────────────────────────
    print("\n[Postprocess] Loading best checkpoint and collecting predictions ...")
    checkpoint = torch.load(ckpt_file, map_location=device)
    model.load_state_dict(checkpoint['model_state_dict'])
    model.eval()

    all_preds = np.empty([0, num_gene])
    all_labels = np.empty([0, num_gene])
    with torch.no_grad():
        for i, batch in enumerate(get_batch_data(
                test_ft, test_lb, batch_size, shuffle=False,
                device=device, ablation_mode=args.ablation_mode)):
            ft, lb = batch
            drug = ft['drug']
            cell_id = ft.get('cell_id', None)
            gene = ft['gene']
            predict = model(drug, gene, None, cell_id, None)
            all_preds = np.concatenate(
                (all_preds, predict.cpu().numpy()), axis=0)
            all_labels = np.concatenate(
                (all_labels, lb['real'].cpu().numpy()), axis=0)

    # ── Save unified output ────────────────────────────────────────────────
    pred_dir = os.path.join(out_dir, "predictions")
    log_dir = os.path.join(out_dir, "logs")
    os.makedirs(pred_dir, exist_ok=True)
    os.makedirs(log_dir, exist_ok=True)

    # x_deg predictions and ground truth
    test_predictions = all_preds.astype(np.float32)
    test_ground_truth = all_labels.astype(np.float32)
    test_ctrl = meta['x_ctrl'].astype(np.float32)

    np.save(os.path.join(pred_dir, "test_predictions.npy"), test_predictions)
    np.save(os.path.join(pred_dir, "test_ground_truth.npy"), test_ground_truth)
    np.save(os.path.join(pred_dir, "test_ctrl.npy"), test_ctrl)
    np.save(os.path.join(pred_dir, "train_x_pert_mean.npy"),
            meta['x_pert_train_mean'].astype(np.float32))

    # test_sample_ids.csv
    sample_ids = pd.DataFrame({
        'drug_id': meta['pert_idx'],
        'cell_id': meta['cell_idx'],
    })
    sample_ids.to_csv(os.path.join(pred_dir, "test_sample_ids.csv"), index=False)

    # per_drug_pcc.csv
    drug_ids = sample_ids['drug_id'].values
    unique_drugs = np.unique(drug_ids)
    drug_pccs = []
    for drug in unique_drugs:
        dmask = drug_ids == drug
        pccs = []
        for i in np.where(dmask)[0]:
            gt, pr = test_ground_truth[i], test_predictions[i]
            if np.std(gt) > 0 and np.std(pr) > 0:
                r, _ = pearsonr(gt, pr)
                pccs.append(r)
        if pccs:
            drug_pccs.append({'drug_id': int(drug), 'pcc': float(np.mean(pccs)),
                              'n_samples': int(dmask.sum())})
    pcc_df = pd.DataFrame(drug_pccs)
    pcc_df.to_csv(os.path.join(pred_dir, "per_drug_pcc.csv"), index=False)
    mean_pcc = pcc_df['pcc'].mean() if len(pcc_df) > 0 else float('nan')
    print(f"Mean PCC_deg: {mean_pcc:.4f}")

    # resource_log.json
    resource_log = {
        "model": "ciger",
        "fold": fold,
        "ablation_mode": args.ablation_mode,
        "seed": seed,
        "best_epoch": int(best_epoch + 1),
        "best_dev_ndcg": float(best_dev_ndcg),
        "wall_time_s": int(wall_time),
        "param_count": param_count,
        "n_train": len(train_ft['drug']),
        "n_dev": len(dev_ft['drug']),
        "n_test": len(test_ft['drug']),
        "mean_pcc_deg": float(mean_pcc),
        "hostname": os.uname().nodename,
        "slurm_job_id": os.environ.get("SLURM_JOB_ID", "N/A"),
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
    }
    with open(os.path.join(log_dir, "resource_log.json"), 'w') as f:
        json.dump(resource_log, f, indent=2)

    end_time = datetime.now()
    print(f"\n{'='*60}")
    print(f"Fold {fold} complete!")
    print(f"Output: {out_dir}")
    print(f"Elapsed: {end_time - start_time}")
    print(f"{'='*60}")


if __name__ == "__main__":
    main()
