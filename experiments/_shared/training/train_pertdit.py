"""
PertDiT training — supports per-sample dose for original-dataset reproduction.

Adds --split_col / --dose_col to support both the unified L1000 dataset and
the original-dataset reproductions. Uses bench_dataset.py from PertDiT, which
supports per-sample dose embedding lookup.

Usage:
    python experiments/_shared/training/train_pertdit.py --fold 0 --split_col drug_split_0 --dose_col dose \\
        --h5ad_path .../prnet/prnet_original.h5ad --drug_emb_path .../p6_drug_emb.pkl \\
        --dose_emb_path .../p6_dose_emb.pkl
"""

# Suppress RDKit's verbose C++ logger BEFORE any rdkit-using import
# (otherwise GetExplicitValence deprecation spams stderr ~5M lines/run).
from rdkit import RDLogger
RDLogger.logger().setLevel(RDLogger.ERROR)
RDLogger.DisableLog('rdApp.*')

import argparse
import json
import os
import sys
import time
from collections import defaultdict

import numpy as np
import pandas as pd
import scanpy as sc
import torch
import torch.nn as nn
from scipy.stats import pearsonr

from pathlib import Path
# PertDiT model code: dpb/models/PertDiT/repo/src/{model,sampler,trainer,utils}/
PERTDIT_ROOT = str(Path(__file__).resolve().parents[3] / "models" / "PertDiT" / "repo" / "src")
sys.path.insert(0, PERTDIT_ROOT)
# Helper modules live alongside this script

# PertDiT modules (copied from original, zero modifications)
from model.CrossDit import Crossformer
from sampler.Sampler import Diffusion_Sampler
from trainer.optimizer import get_optimizer_scheduler
from utils.seed_everything import seed_everything
from bench_dataset import BenchPertDiTDataset, build_dataloaders


# ── Paths ─────────────────────────────────────────────────────────────────────
REPO_ROOT = os.environ.get("DATA_ROOT", str(Path(__file__).resolve().parents[3]))
H5AD_DEFAULT = os.path.join(REPO_ROOT, "data/XPert/processed_data/l1000_sdst_78453.h5ad")
RESULTS_BASE = os.path.join(REPO_ROOT, "results")
# PertDiT drug + dose embeddings (3.7 GB) are generated by
# experiments/main_benchmark/data_prep/prepare_pertdit_embeddings.py and land
# in data/PertDiT/bench_drug_emb/.  Run prepare.py before training.
DRUG_EMB_DEFAULT = os.path.join(REPO_ROOT, "data/PertDiT/bench_drug_emb/bench_drug_emb.pkl")
DOSE_EMB_DEFAULT = os.path.join(REPO_ROOT, "data/PertDiT/bench_drug_emb/bench_dose_emb.pt")


def parse_args():
    p = argparse.ArgumentParser(description="PertDiT (CrossDiT) cold-drug training")
    p.add_argument("--fold", type=int, required=True, help="Fold index 0-4")
    p.add_argument("--split_col", type=str, default=None,
                   help="Override split column name (default: nm_drug_blind_<fold+1>; pass e.g. nm_scaffold_1 to use scaffold-disjoint splits)")
    p.add_argument("--output_dir", type=str, default=None)
    p.add_argument("--h5ad_path", type=str, default=H5AD_DEFAULT)
    p.add_argument("--drug_emb_path", type=str, default=DRUG_EMB_DEFAULT)
    p.add_argument("--dose_emb_path", type=str, default=DOSE_EMB_DEFAULT)
    p.add_argument("--dose_col", type=str, default=None,
                   help="Per-sample dose column in h5ad obs (original-dataset multi-dose support)")
    p.add_argument("--dev", type=str, default="cuda:0")

    # PertDiT paper defaults (from _repro_drug_s117.yaml)
    p.add_argument("--seed", type=int, default=117)
    p.add_argument("--n_epochs", type=int, default=1000)
    p.add_argument("--batch_size", type=int, default=64)
    p.add_argument("--early_stop_patience", type=int, default=10)
    p.add_argument("--min_epochs", type=int, default=60,
                   help="Minimum epochs before early stopping activates (config.train.patience)")

    # Scheduler (from _repro_drug_s117.yaml)
    p.add_argument("--lr_start", type=float, default=0.0005)
    p.add_argument("--lr_max", type=float, default=0.001)
    p.add_argument("--lr_min", type=float, default=5e-5)
    p.add_argument("--warmup_steps", type=int, default=5000)
    p.add_argument("--T_max", type=int, default=1000000)

    # Diffusion (from _repro_drug_s117.yaml)
    p.add_argument("--train_steps", type=int, default=50)
    p.add_argument("--num_steps", type=int, default=50)
    p.add_argument("--beta_start", type=float, default=0.002)
    p.add_argument("--beta_end", type=float, default=0.4)
    p.add_argument("--beta_schedule", type=str, default="scaled_linear")
    p.add_argument("--guidance_scale", type=float, default=3.0)

    # Model (from _repro_drug_s117.yaml)
    p.add_argument("--d_model", type=int, default=256)
    p.add_argument("--d_cond", type=int, default=1024)
    p.add_argument("--num_layers", type=int, default=2)
    p.add_argument("--num_heads", type=int, default=4)
    p.add_argument("--mlp_hidden_dim", type=int, default=256)
    p.add_argument("--dropout", type=float, default=0.0)

    # Batch-cap & validation sampling (from original Cross.yaml)
    p.add_argument("--train_batchs", type=int, default=5000,
                   help="Max training batches per epoch (-1=full); original Cross.yaml: 5000")
    p.add_argument("--valid_batchs", type=int, default=110,
                   help="Max validation batches per epoch (-1=full); original Cross.yaml: 110")
    p.add_argument("--valid_sampling_step", type=int, default=5,
                   help="Take every N-th validation sample; original Cross.yaml: 5")
    p.add_argument("--test_batchs", type=int, default=-1,
                   help="Max test batches (-1=full); original Cross.yaml: -1")

    # Ablation
    p.add_argument("--ablation_mode", type=str, default="none",
                   choices=["none", "zero", "shuffle"])

    return p.parse_args()


def main():
    args = parse_args()
    fold_idx = args.fold
    split_col = args.split_col or f"nm_drug_blind_{fold_idx + 1}"

    out_dir = args.output_dir or os.path.join(
        RESULTS_BASE, f"main_benchmark/pertdit/fold_{fold_idx}")

    print(f"{'='*60}")
    print(f"PertDiT (CrossDiT) | Fold {fold_idx} | Split: {split_col}")
    print(f"Output: {out_dir}")
    print(f"Seed: {args.seed} | Ablation: {args.ablation_mode}")
    print(f"{'='*60}")

    # Seed everything (matches PertDiT's seed_everything call)
    seed_everything(args.seed)
    torch.set_num_threads(4)  # match original PertDiT main.py line 41
    dev = torch.device(args.dev if torch.cuda.is_available() else "cpu")
    relu = nn.ReLU()

    # ── Step 1: Load data ─────────────────────────────────────────────────────
    print("\n[1/7] Loading data ...")
    adata = sc.read_h5ad(args.h5ad_path)

    # PertDiT original preprocessing (Trainer.py:43-45)
    if hasattr(adata.X, 'toarray'):
        adata.X = adata.X.toarray()
    adata.X = np.clip(adata.X, 0, 1e3)
    sc.pp.normalize_total(adata)
    sc.pp.log1p(adata)

    # Preprocess control expression separately (obsm not affected by sc.pp)
    import anndata
    ctrl_raw = adata.obsm['X_ctl']
    if hasattr(ctrl_raw, 'toarray'):
        ctrl_raw = ctrl_raw.toarray()
    ctrl_raw = np.clip(ctrl_raw, 0, 1e3)
    adata_ctrl = anndata.AnnData(X=ctrl_raw.copy())
    sc.pp.normalize_total(adata_ctrl)
    sc.pp.log1p(adata_ctrl)

    x_pert = adata.X
    if hasattr(x_pert, 'toarray'):
        x_pert = x_pert.toarray()
    x_ctrl = adata_ctrl.X
    if hasattr(x_ctrl, 'toarray'):
        x_ctrl = x_ctrl.toarray()
    print(f"  Data after preprocessing: x_pert range [{x_pert.min():.2f}, {x_pert.max():.2f}], "
          f"x_ctrl range [{x_ctrl.min():.2f}, {x_ctrl.max():.2f}]")

    pert_idx_all = adata.obs['pert_idx'].values.astype(int)
    cell_idx_all = adata.obs['cell_idx'].values.astype(int)
    splits = adata.obs[split_col].values.astype(str)

    # ── Step 2: Load drug embeddings ──────────────────────────────────────────
    print("\n[2/7] Loading drug embeddings ...")
    drug_emb_dict = torch.load(args.drug_emb_path, map_location='cpu')
    dose_emb = torch.load(args.dose_emb_path, map_location='cpu')
    print(f"  Drug embeddings: {len(drug_emb_dict)-1} drugs + negative_ctrl")
    if args.dose_col:
        assert isinstance(dose_emb, dict), "dose_emb must be a dict when --dose_col is set"
        print(f"  Dose embedding: dict with {len(dose_emb)} dose levels")
    else:
        print(f"  Dose embedding: shape={dose_emb.shape}")

    # ── Ablation ──────────────────────────────────────────────────────────────
    if args.ablation_mode == 'zero':
        print("  [ABLATION] Zeroing drug embeddings (preserving dose)")
        for k in list(drug_emb_dict.keys()):
            if k != 'negative_ctrl':
                drug_emb_dict[k] = torch.zeros_like(drug_emb_dict[k])
    elif args.ablation_mode == 'shuffle':
        print("  [ABLATION] Shuffling drug identity (permutation seed=131419)")
        unique_drugs = np.unique(pert_idx_all)
        rng = np.random.default_rng(seed=131419)
        perm = rng.permutation(len(unique_drugs))
        drug_map = dict(zip(unique_drugs, unique_drugs[perm]))
        # Build shuffled dict: original idx → embedding of shuffled idx
        shuffled_dict = {'negative_ctrl': drug_emb_dict['negative_ctrl']}
        for orig_idx, shuf_idx in drug_map.items():
            shuffled_dict[orig_idx] = drug_emb_dict[shuf_idx]
        drug_emb_dict = shuffled_dict

    # ── Step 3: Split + DataLoaders ───────────────────────────────────────────
    print("\n[3/7] Building DataLoaders ...")
    train_mask = splits == 'train'
    valid_mask = splits == 'valid'
    test_mask = splits == 'test'
    print(f"  train={train_mask.sum()}, valid={valid_mask.sum()}, test={test_mask.sum()}")

    # Per-sample dose (multi-dose from original data)
    if args.dose_col:
        dose_values = adata.obs[args.dose_col].values.astype(float)
        print(f"  Per-sample dose from '{args.dose_col}': "
              f"min={dose_values.min():.4f}, max={dose_values.max():.4f}, "
              f"unique={len(set(dose_values))}")
    else:
        dose_values = None

    # CFG disabled (using_cfg: false in original config)
    train_ds = BenchPertDiTDataset(
        x_ctrl[train_mask], x_pert[train_mask], pert_idx_all[train_mask],
        drug_emb_dict, dose_emb,
        dose_list=dose_values[train_mask] if dose_values is not None else None, cfg=False)
    valid_ds = BenchPertDiTDataset(
        x_ctrl[valid_mask], x_pert[valid_mask], pert_idx_all[valid_mask],
        drug_emb_dict, dose_emb,
        dose_list=dose_values[valid_mask] if dose_values is not None else None, cfg=False)
    test_ds = BenchPertDiTDataset(
        x_ctrl[test_mask], x_pert[test_mask], pert_idx_all[test_mask],
        drug_emb_dict, dose_emb,
        dose_list=dose_values[test_mask] if dose_values is not None else None, cfg=False)

    # Validation subsampling (Trainer.py:68-71, valid_sampling_step from Cross.yaml)
    if args.valid_sampling_step != 1:
        indices = list(range(0, len(valid_ds), args.valid_sampling_step))
        valid_ds = torch.utils.data.Subset(valid_ds, indices)
        print(f"  valid after sampling_step={args.valid_sampling_step}: {len(valid_ds)}")

    train_loader, valid_loader, test_loader = build_dataloaders(
        train_ds, valid_ds, test_ds, batch_size=args.batch_size)

    # Batch-cap (Trainer.py:132-134, from Cross.yaml)
    train_batchs = min(args.train_batchs, len(train_loader)) if args.train_batchs != -1 else len(train_loader)
    valid_batchs = min(args.valid_batchs, len(valid_loader)) if args.valid_batchs != -1 else len(valid_loader)
    test_batchs = min(args.test_batchs, len(test_loader)) if args.test_batchs != -1 else len(test_loader)
    print(f"  Batch caps: train={train_batchs}/{len(train_loader)}, "
          f"valid={valid_batchs}/{len(valid_loader)}, test={test_batchs}/{len(test_loader)}")

    # ── Step 4: Create output dirs ────────────────────────────────────────────
    ckpt_dir = os.path.join(out_dir, "checkpoints")
    for d in [os.path.join(out_dir, "predictions"),
              os.path.join(out_dir, "logs"),
              os.path.join(out_dir, "metrics"),
              ckpt_dir]:
        os.makedirs(d, exist_ok=True)

    # ── Step 5: Init model + sampler + optimizer ──────────────────────────────
    print("\n[4/7] Initializing model ...")

    # Crossformer: seed_everything(117) is called inside __init__
    model = Crossformer(
        num_steps=args.train_steps,
        num_layers=args.num_layers,
        d_model=args.d_model,
        d_cond=args.d_cond,
        d_pre=978,
        mlp_hidden_dim=args.mlp_hidden_dim,
        num_heads=args.num_heads,
        dropout=args.dropout,
        nonorm=False
    )
    model.init_weights()  # Xavier init, same as Trainer.py line 92
    model = model.to(dev)


    param_count = sum(p.numel() for p in model.parameters())
    print(f"  Parameters: {param_count:,}")

    # Diffusion sampler (DDPM, no CFG since using_cfg=False)
    sampler = Diffusion_Sampler(
        sampler_type="DDPM",
        num_train_timesteps=args.train_steps,
        timesteps=args.num_steps,
        start=args.beta_start,
        end=args.beta_end,
        beta_schedule=args.beta_schedule,
        device=dev,
        guidance_scale=args.guidance_scale,
        uncond=None  # using_cfg=False
    )

    # Optimizer + cosine warmup scheduler (matches Trainer.py lines 99-106)
    optimizer, scheduler = get_optimizer_scheduler(
        model.parameters(),
        lr_max=args.lr_max,
        warmup_n_steps=args.warmup_steps,
        lr_start=args.lr_start,
        T_max=args.T_max,
        lr_min=args.lr_min
    )

    # ── Step 6: Train ─────────────────────────────────────────────────────────
    print(f"\n[5/7] Training (max {args.n_epochs} epochs, early stop patience="
          f"{args.early_stop_patience} after epoch {args.min_epochs}) ...")
    start_time = time.time()

    best_mse = float('inf')
    best_epoch = 0
    epoch_hist = defaultdict(list)

    for epoch in range(args.n_epochs):
        # ── Train ──
        model.train()
        train_losses = []
        for i, batch in enumerate(train_loader):
            if i == train_batchs:
                break
            x_pert_b, x_ctrl_b, drug_emb_b, mask = batch
            x_pert_b = x_pert_b.to(dev)
            x_ctrl_b = x_ctrl_b.to(dev)
            drug_emb_b = drug_emb_b.to(dev)

            # Cross_loss_func: sampler.diffusion_loss_fn(model, x_pert, x_ctrl, drug, mask)
            loss = sampler.diffusion_loss_fn(model, x_pert_b, x_ctrl_b, drug_emb_b, mask)

            optimizer.zero_grad()
            loss.backward()
            # Gradient clipping (Trainer.py line 180)
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.)
            optimizer.step()
            # Scheduler step per batch (Trainer.py line 182)
            scheduler.step()

            train_losses.append(loss.item())

        avg_train_loss = sum(train_losses) / len(train_losses)
        epoch_hist['train_loss'].append(avg_train_loss)

        # ── Validate (every epoch, matching Trainer.py line 199) ──
        model.eval()
        val_mses = []
        with torch.no_grad():
            for vi, batch in enumerate(valid_loader):
                if vi == valid_batchs:
                    break
                x_pert_b, x_ctrl_b, drug_emb_b, mask = batch
                x_pert_b = x_pert_b.to(dev)
                x_ctrl_b = x_ctrl_b.to(dev)
                drug_emb_b = drug_emb_b.to(dev)

                # Cross_generator: ddim_sample
                y_pred = sampler.ddim_sample(
                    model, x_pert_b.shape, x_ctrl_b, drug_emb_b, mask).cpu()
                y_true = x_pert_b.cpu()

                # ReLU on predictions (Trainer.py line 241, using_FC=False)
                y_pred = relu(y_pred)

                # Per-sample MSE, then mean (matches Trainer.py mse() function)
                mse = torch.mean((y_true - y_pred) ** 2, dim=1)  # (B,)
                val_mses.append(mse)

        val_mse = torch.cat(val_mses).mean().item()
        epoch_hist['val_mse'].append(val_mse)

        if epoch % 10 == 0 or epoch < 5:
            print(f"  [Epoch {epoch:3d}] train_loss={avg_train_loss:.6f}, val_mse={val_mse:.4f}, "
                  f"lr={optimizer.param_groups[0]['lr']:.2e}")

        # ── Early stopping (Trainer.py line 201) ──
        if val_mse < best_mse:
            best_mse = val_mse
            best_epoch = epoch
            torch.save({
                'model_state_dict': model.state_dict(),
                'epoch': epoch,
                'best_mse': best_mse,
            }, os.path.join(ckpt_dir, "best_model.pt"))

        # Early stop condition: no improvement for patience epochs AND past min_epochs
        if (epoch - best_epoch >= args.early_stop_patience
                and epoch > args.min_epochs):
            print(f"  Early stopping at epoch {epoch} "
                  f"(no improvement since epoch {best_epoch})")
            break

    train_time = time.time() - start_time
    print(f"  Best epoch: {best_epoch}, Best val MSE: {best_mse:.4f}")
    print(f"  Wall time: {train_time:.0f}s ({train_time/60:.1f}min)")

    # Save training curve
    curve_df = pd.DataFrame(epoch_hist)
    curve_df['epoch'] = range(len(curve_df))
    curve_df.to_csv(os.path.join(out_dir, "logs", "training_curve.csv"), index=False)

    # ── Step 7: Predict on test set ───────────────────────────────────────────
    print("\n[6/7] Predicting on test set ...")
    ckpt = torch.load(os.path.join(ckpt_dir, "best_model.pt"), map_location=dev)
    model.load_state_dict(ckpt['model_state_dict'])
    model.eval()

    # Reset seed for reproducible sampling
    seed_everything(args.seed)

    all_ctrl = []
    all_target = []
    all_pred = []

    with torch.no_grad():
        for ti, batch in enumerate(test_loader):
            if ti == test_batchs:
                break
            x_pert_b, x_ctrl_b, drug_emb_b, mask = batch
            x_pert_b = x_pert_b.to(dev)
            x_ctrl_b = x_ctrl_b.to(dev)
            drug_emb_b = drug_emb_b.to(dev)

            y_pred = sampler.ddim_sample(
                model, x_pert_b.shape, x_ctrl_b, drug_emb_b, mask).cpu()
            # ReLU on predictions (absolute expression should be non-negative)
            y_pred = relu(y_pred)

            all_ctrl.append(x_ctrl_b.cpu().numpy())
            all_target.append(x_pert_b.cpu().numpy())
            all_pred.append(y_pred.numpy())

    x_ctrl_test = np.concatenate(all_ctrl, axis=0)
    x_pert_test = np.concatenate(all_target, axis=0)
    x_pred_test = np.concatenate(all_pred, axis=0)

    print(f"  Shapes: ctrl={x_ctrl_test.shape}, pert={x_pert_test.shape}, pred={x_pred_test.shape}")

    # ── Step 8: Save unified output ───────────────────────────────────────────
    print("\n[7/7] Saving unified output ...")
    pred_dir = os.path.join(out_dir, "predictions")

    # x_deg = x_pred - x_ctrl (differential expression)
    test_predictions = (x_pred_test - x_ctrl_test).astype(np.float32)
    test_ground_truth = (x_pert_test - x_ctrl_test).astype(np.float32)
    test_ctrl = x_ctrl_test.astype(np.float32)

    np.save(os.path.join(pred_dir, "test_predictions.npy"), test_predictions)
    np.save(os.path.join(pred_dir, "test_ground_truth.npy"), test_ground_truth)
    np.save(os.path.join(pred_dir, "test_ctrl.npy"), test_ctrl)

    # train_x_pert_mean
    train_x_pert_mean = x_pert[train_mask].mean(axis=0).astype(np.float32)
    np.save(os.path.join(pred_dir, "train_x_pert_mean.npy"), train_x_pert_mean)

    # test_sample_ids.csv
    sample_ids = pd.DataFrame({
        'drug_id': pert_idx_all[test_mask],
        'cell_id': cell_idx_all[test_mask]
    })
    sample_ids.to_csv(os.path.join(pred_dir, "test_sample_ids.csv"), index=False)

    # per_drug_pcc.csv
    drug_ids = sample_ids['drug_id'].values
    unique_drugs = np.unique(drug_ids)
    drug_pccs = []
    for drug in unique_drugs:
        dmask = drug_ids == drug
        pccs = []
        for i in np.where(dmask)[0]:
            gt, pr = test_ground_truth[i], test_predictions[i]
            if np.std(gt) > 0 and np.std(pr) > 0:
                r, _ = pearsonr(gt, pr)
                pccs.append(r)
        if pccs:
            drug_pccs.append({'drug_id': int(drug), 'pcc': float(np.mean(pccs)),
                              'n_samples': int(dmask.sum())})
    pd.DataFrame(drug_pccs).to_csv(os.path.join(pred_dir, "per_drug_pcc.csv"), index=False)

    # Resource log
    resource_log = {
        "model": "pertdit",
        "variant": "CrossDiT",
        "fold": fold_idx,
        "split_col": split_col,
        "seed": args.seed,
        "ablation_mode": args.ablation_mode,
        "best_epoch": int(best_epoch),
        "n_epochs_run": len(epoch_hist['train_loss']),
        "wall_time_s": int(train_time),
        "param_count": param_count,
        "n_train": int(train_mask.sum()),
        "n_valid": int(valid_mask.sum()),
        "n_test": int(test_mask.sum()),
        "train_batchs": train_batchs,
        "valid_batchs": valid_batchs,
        "valid_sampling_step": args.valid_sampling_step,
        "hostname": os.uname().nodename,
        "slurm_job_id": os.environ.get("SLURM_JOB_ID", "N/A"),
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S")
    }
    with open(os.path.join(out_dir, "logs", "resource_log.json"), 'w') as f:
        json.dump(resource_log, f, indent=2)

    print(f"\n{'='*60}")
    print(f"Fold {fold_idx} complete!")
    print(f"  Predictions: {test_predictions.shape}")
    print(f"  Best epoch: {best_epoch}, Val MSE: {best_mse:.4f}")
    print(f"  Wall time: {train_time:.0f}s ({train_time/60:.1f}min)")
    print(f"  Output: {out_dir}")
    print(f"{'='*60}")


if __name__ == "__main__":
    main()
