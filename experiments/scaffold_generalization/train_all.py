#!/usr/bin/env python
"""Run scaffold_generalization: scaffold-disjoint 5-fold,
optionally with drug-zero ablation overlay.

Same h5ad as main_benchmark (L1000_sdst) but uses `nm_scaffold_<N>` split columns
instead of `nm_drug_blind_<N>` — tests generalization to drug scaffolds
unseen in training.

Two ablation modes are run by default:
    * ``none`` — full drug input (scaffold benchmark).
    * ``zero`` — replace drug atom features with zeros (scaffold-disjoint
                 drug-blind ablation).

Note: shuffle ablation is not part of scaffold_generalization; only main_ablation
and original_ablation cover the shuffle mode.

Quick verification (1 epoch, fold 0, 7 models × 2 ablations = 14 runs):
    python experiments/scaffold_generalization/train_all.py --epochs 1 --folds 0

Single model, single ablation:
    python experiments/scaffold_generalization/train_all.py --models ciger --ablation_modes none

Full reproduction (paper epochs × all 5 folds, 70 runs total):
    python experiments/scaffold_generalization/train_all.py

Output layout:
    results/scaffold_generalization/<model>/<ablation>/fold_<N>/{checkpoints,
                                                                 predictions,
                                                                 metrics, logs}

XPert is included via experiments/_shared/training/train_xpert.py — the shim
maps --ablation_mode {none, zero} to XPert's pre-shipped
config_l1000{,_ablation_zero_all}.yaml configs (the zero drug arrays are
pre-generated by experiments/scaffold_generalization/prepare.py).
"""
from __future__ import annotations

import argparse
import shlex
import subprocess
import sys
import time
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
TRAIN_DIR = REPO_ROOT / "experiments" / "_shared" / "training"
EVAL = REPO_ROOT / "experiments" / "_shared" / "evaluation" / "evaluate_one_fold.py"
DEFAULT_RESULTS = REPO_ROOT / "results" / "scaffold_generalization"

MODELS = {
    "ciger":     {"train": "train_ciger.py",     "epoch_arg": "--max_epoch"},
    "deepce":    {"train": "train_deepce.py",    "epoch_arg": "--max_epoch"},
    "multidcp":  {"train": "train_multidcp.py",  "epoch_arg": "--max_epoch"},
    "pertdit":   {"train": "train_pertdit.py",   "epoch_arg": "--n_epochs"},
    "prnet":     {"train": "train_prnet.py",     "epoch_arg": "--n_epochs"},
    "transigen": {"train": "train_transigen.py", "epoch_arg": "--n_epochs"},
    "xpert":     {"train": "train_xpert.py",     "epoch_arg": "--max_epoch"},
}

ABLATION_CHOICES = ("none", "zero")
DEFAULT_ABLATION_MODES = ["none", "zero"]
# scaffold_generalization uses nm_scaffold_<fold+1> instead of nm_drug_blind_<fold+1>
SPLIT_COL_TEMPLATE = "nm_scaffold_{fold_one_indexed}"


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Run scaffold_generalization scaffold-disjoint reproduction without SLURM.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__.split("Quick verification")[1].rsplit("XPert", 1)[0],
    )
    p.add_argument(
        "--models", nargs="+", default=list(MODELS),
        choices=list(MODELS),
        help="Model subset (default: all 7).",
    )
    p.add_argument(
        "--folds", type=int, nargs="+", default=[0, 1, 2, 3, 4],
        help="Fold indices 0..4 (default: all).",
    )
    p.add_argument(
        "--epochs", type=int, default=None,
        help="Override training epochs (default: each model's paper default).",
    )
    p.add_argument(
        "--ablation_modes", nargs="+", default=DEFAULT_ABLATION_MODES,
        choices=list(ABLATION_CHOICES),
        help="Ablation modes to run (default: none zero).",
    )
    p.add_argument(
        "--results_dir", type=Path, default=DEFAULT_RESULTS,
        help=f"Output base dir (default: {DEFAULT_RESULTS.relative_to(REPO_ROOT)})",
    )
    p.add_argument(
        "--dev", default="cuda:0",
        help="PyTorch device string (default: cuda:0).",
    )
    p.add_argument(
        "--continue_on_fail", action="store_true",
        help="If a model×ablation×fold fails, log and continue instead of stopping.",
    )
    return p.parse_args()


def run_one(model: str, ablation: str, fold: int,
            args: argparse.Namespace) -> tuple[str, int, float]:
    cfg = MODELS[model]
    split_col = SPLIT_COL_TEMPLATE.format(fold_one_indexed=fold + 1)
    outdir = args.results_dir / model / ablation / f"fold_{fold}"
    outdir.mkdir(parents=True, exist_ok=True)
    cmd = [
        sys.executable, str(TRAIN_DIR / cfg["train"]),
        "--fold", str(fold),
        "--split_col", split_col,
        "--ablation_mode", ablation,
        "--output_dir", str(outdir),
        "--dev", args.dev,
    ]
    if args.epochs is not None:
        cmd += [cfg["epoch_arg"], str(args.epochs)]

    label = f"{model} {ablation} fold_{fold}"
    print(f"\n{'='*72}")
    print(f"  RUN  {label}  ({split_col})")
    print(f"  CMD  {shlex.join(cmd)}")
    print(f"{'='*72}", flush=True)

    t0 = time.time()
    try:
        subprocess.run(cmd, check=True, cwd=REPO_ROOT)
        rc = 0
        # Auto-evaluate: produce metrics/all_metrics.json
        print(f"\n  [EVAL] {label}")
        subprocess.run([sys.executable, str(EVAL), str(outdir)],
                       check=True, cwd=REPO_ROOT)
    except subprocess.CalledProcessError as e:
        rc = e.returncode
        if not args.continue_on_fail:
            raise
        print(f"\n[WARN] {label} exited with code {rc}; continuing per --continue_on_fail")
    return label, rc, time.time() - t0


def main() -> None:
    args = parse_args()
    print(f"scaffold_generalization driver — scaffold-disjoint reproduction")
    print(f"  repo root      : {REPO_ROOT}")
    print(f"  results dir    : {args.results_dir}")
    print(f"  models         : {args.models}")
    print(f"  ablation_modes : {args.ablation_modes}")
    print(f"  folds          : {args.folds}")
    print(f"  epochs         : {args.epochs if args.epochs is not None else 'paper defaults'}")
    print(f"  device         : {args.dev}")

    summary: list[tuple[str, int, float]] = []
    for model in args.models:
        for ablation in args.ablation_modes:
            for fold in args.folds:
                summary.append(run_one(model, ablation, fold, args))

    print(f"\n{'='*72}")
    print(f"  DONE — {len(summary)} runs total")
    print(f"{'='*72}")
    for label, rc, secs in summary:
        status = "OK" if rc == 0 else f"FAIL rc={rc}"
        print(f"  {status:<10} {secs:>7.1f}s  {label}")

    failed = [s for s in summary if s[1] != 0]
    if failed:
        sys.exit(1)


if __name__ == "__main__":
    main()
